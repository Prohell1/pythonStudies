# Data Types
# String
print("Hello"[4])  # prints o
print("123" + "345")  # prints 123345 (concatenation)

# Integer
print(123 + 345)  # prints 468 (the sum of the two integers)
print(123_456_789)  # prints 123456789 (underscore is used for readability, but does not affect the interpreter)

# Float
print(3.14159)  # floating point numbers have a decimal point, somewhere to identify it

# Boolean
print(True)
print(False)
