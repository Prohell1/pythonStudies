print(3 + 5)   # adding numbers - prints 8
print(7 - 4)   # subtracting numbers - prints 3
print(3 * 2)   # multiplying numbers - prints 6
print(6 / 3)   # dividing numbers - prints 2.0 (result is always float)
print(2 ** 3)  # raising the power of a number - prints 8

# PEMDAS rule is applied, like in maths () => ** => * / => + -
print(3 * 3 + 3 / 3 - 3)    # prints 7.0
print(3 * (3 + 3) / 3 - 3)  # prints 3.0


print(round(8 / 3, 2))  # prints 2.67, because it rounds the number to 2 decimal places
print(8 // 3)           # prints 2, (floor division), result is always an integer


result = 4 / 2  # saving the result to a variable
result /= 2     # we reassign the result with a new number after the operation was made
print(result)   # prints 1.0

